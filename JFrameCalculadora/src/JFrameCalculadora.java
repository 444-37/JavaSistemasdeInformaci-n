    private void jButtonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIgualActionPerformed
        // la variable float solicita que se inicialise, y se le agrega la "f" para indicar que se trata de un float
        float floatResultado = 0.0f;
        //La variable resultado va a guardar el resultado de la operaciÃ³n. Entendiendo que ambas variables son de tipo "float" el resultado igual.
        String displayText = jLabelDisplay.getText();
        this.segundoNumero = Float.parseFloat(displayText);

        //Aqui se evalua el operador, en vez de poner muchos "if" usamos "switch". Para que no siga corriendo el programa cuando ya encontro el operador necesario necesitamos un "break"
        switch (this.operador) {
            case "+" -> {
                floatResultado = this.primerNumero + this.segundoNumero;
                break;
            }
            case "-" -> {
                floatResultado = this.primerNumero - this.segundoNumero;
                break;
            }
            case "*" -> {
                floatResultado = this.primerNumero * this.segundoNumero;
                break;
            }
            case "/" -> {
                if (this.segundoNumero == 0) {
                    jLabelDisplay.setText("No se puede dividir por 0");
                    return;
                }

                floatResultado = this.primerNumero / this.segundoNumero;
            }

        }

        //Aqui se guardo el resultado tipo "float" lo convertimos a "string" y se guartda en una variable del mismo tipo.
        String stringResultado = Float.toString(floatResultado);
        /*
        OTRA FORMA EN EL CASO DE LA DIVISIÃ“N:
        if(stringResultado.equalsIgnoreCase("Infinity")){
            stringResultado = "No se puede dividir por 0";
        }
        */
        jLabelDisplay.setText(stringResultado);
    }//GEN-LAST:event_jButtonIgualActionPerformed

