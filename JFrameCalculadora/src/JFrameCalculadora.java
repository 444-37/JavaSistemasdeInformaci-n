    private void jPanel1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPanel1KeyPressed
        char keyChar = evt.getKeyChar();
        if (Character.isDigit(keyChar) || keyChar == '+' || keyChar == '-' || keyChar == '*' || keyChar == '/') {
            jLabelDisplay.setText(jLabelDisplay.getText() + keyChar);
        } else if (keyChar == KeyEvent.VK_ENTER) {
            jButtonIgual.doClick(); // Simula el clic en el botÃ³n de igual
        } else if (keyChar == KeyEvent.VK_BACK_SPACE) {
            String currentText = jLabelDisplay.getText();
            if (currentText.length() > 0) {
                jLabelDisplay.setText(currentText.substring(0, currentText.length() - 1));
            }
        }
    }//GEN-LAST:event_jPanel1KeyPressed

